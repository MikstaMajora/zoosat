CREATE TABLE IF NOT EXISTS `ppds_courier` 
(
	`ID` int NOT NULL AUTO_INCREMENT,
	`ORDER_NUMBER` varchar(20) NULL,
	`IKN` varchar(12) NOT NULL,
	`CITY` varchar(255) NOT NULL,
	`CITY_LINK` int(10) NOT NULL DEFAULT 0,
	`ADDRESS` text NOT NULL,
	`FIO` varchar(255) NOT NULL,
	`PHONE` varchar(255) NOT NULL,
	`DATE` datetime NOT NULL,
	`TIME_START` int(5) NOT NULL DEFAULT 0,
	`TIME_END` int(5) NOT NULL DEFAULT 0,
	`NUMBER` int(5) NOT NULL DEFAULT 1,
	`WEIGHT` int(5) NOT NULL DEFAULT 1,
	`COMMENT` text NULL,
	PRIMARY KEY (`ID`)
) AUTO_INCREMENT = 1;